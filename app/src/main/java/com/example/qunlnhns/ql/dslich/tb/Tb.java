package com.example.qunlnhns.ql.dslich.tb;

public class Tb {
    private String tb;
    private String time;

    public String getTb() {
        return tb;
    }

    public void setTb(String tb) {
        this.tb = tb;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Tb(String tb, String time) {
        this.tb = tb;
        this.time = time;
    }
}
